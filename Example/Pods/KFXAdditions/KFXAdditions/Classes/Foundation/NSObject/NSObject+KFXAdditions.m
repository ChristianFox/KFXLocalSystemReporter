- (void) kfx_performBlock: (dispatch_block_t) block
           afterDelay: (NSTimeInterval) delay;
{
    dispatch_after(dispatch_time(DISPATCH_TIME_NOW, delay * NSEC_PER_SEC), dispatch_get_main_queue(),
                   block);
}


