//--------------------------------------------------------
-(id<NSCoding>)kfx_deepCopy{
    
    id deepCopy = [NSKeyedUnarchiver unarchiveObjectWithData:[NSKeyedArchiver archivedDataWithRootObject:self]];
    return deepCopy;
}







